type OpenAPI struct {
	apiserver apiserver.APIServer
	reflector *openapi3.Reflector
	collector *handler.OpenAPICollector
}

func NewOpenAPI(ctx context.Context, instrumentation instrumentation.Instrumentation) (*OpenAPI, error) {
	apiserver, err := signozapiserver.NewFactory(
		struct{ organization.Getter }{},
		struct{ authz.AuthZ }{},
		struct{ organization.Handler }{},
		struct{ user.Handler }{},
		struct{ session.Handler }{},
		struct{ authdomain.Handler }{},
		struct{ preference.Handler }{},
		struct{ global.Handler }{},
		struct{ promote.Handler }{},
		struct{ flagger.Handler }{},
		struct{ dashboard.Module }{},
		struct{ dashboard.Handler }{},
		struct{ metricsexplorer.Handler }{},
		struct{ inframonitoring.Handler }{},
		struct{ gateway.Handler }{},
		struct{ fields.Handler }{},
		struct{ authz.Handler }{},
		struct{ rawdataexport.Handler }{},
		struct{ zeus.Handler }{},
		struct{ querier.Handler }{},
		struct{ serviceaccount.Handler }{},
		struct{ factory.Handler }{},
		struct{ cloudintegration.Handler }{},
		struct{ rulestatehistory.Handler }{},
		struct{ spanmapper.Handler }{},
		struct{ alertmanager.Handler }{},
		struct{ llmpricingrule.Handler }{},
		struct{ tracedetail.Handler }{},
		struct{ ruler.Handler }{},
	).New(ctx, instrumentation.ToProviderSettings(), apiserver.Config{})
	if err != nil {
		return nil, err
	}

	reflector := openapi3.NewReflector()
	reflector.JSONSchemaReflector().DefaultOptions = append(
		reflector.JSONSchemaReflector().DefaultOptions,
		jsonschema.InterceptDefName(defName),
	)

	reflector.Spec.WithInfo(*(&openapi3.Info{}).
		WithTitle("SigNoz").
		WithDescription("OpenTelemetry-Native Logs, Metrics and Traces in a single pane").
		WithTermsOfService("https://signoz.io/terms-of-service/").
		WithContact(*(&openapi3.Contact{}).
			WithName("SigNoz Support").
			WithURL("https://signoz.io").
			WithEmail("support@signoz.io")),
	)

	reflector.Spec.WithServers(
		// Default server
		*(&openapi3.Server{}).WithURL("https://{host}:{port}{base_path}").
			WithDescription("The fully qualified URL to the SigNoz APIServer.").
			WithVariablesItem("host", *(&openapi3.ServerVariable{}).
				WithDefault("localhost").
				WithDescription("The host of the SigNoz APIServer")).
			WithVariablesItem("port", *(&openapi3.ServerVariable{}).
				WithDefault("8080").
				WithDescription("The port of the SigNoz APIServer")).
			WithVariablesItem("base_path", *(&openapi3.ServerVariable{}).
				WithDefault("/").
				WithDescription("The base path of the SigNoz APIServer")),
	)

	reflector.SpecSchema().SetAPIKeySecurity(authtypes.IdentNProviderAPIKey.StringValue(), "SigNoz-Api-Key", openapi.InHeader, "API Keys")
	reflector.SpecSchema().SetHTTPBearerTokenSecurity(authtypes.IdentNProviderTokenizer.StringValue(), "Tokenizer", "Tokens generated by the tokenizer")

	collector := handler.NewOpenAPICollector(reflector)

	return &OpenAPI{
		apiserver: apiserver,
		reflector: reflector,
		collector: collector,
	}, nil
}

func (openapi *OpenAPI) CreateAndWrite(path string) error {
	if err := openapi.apiserver.Router().Walk(openapi.collector.Walker); err != nil {
		return err
	}

	// The library's MarshalYAML does a JSON round-trip that converts all numbers
	// to float64, causing large integers (e.g. epoch millisecond timestamps) to
	// render in scientific notation (1.6409952e+12).
	jsonData, err := openapi.reflector.Spec.MarshalJSON()
	if err != nil {
		return err
	}

	dec := json.NewDecoder(bytes.NewReader(jsonData))
	dec.UseNumber()

	var v any
	if err := dec.Decode(&v); err != nil {
		return err
	}

	convertJSONNumbers(v)

	spec, err := yaml.Marshal(v)
	if err != nil {
		return err
	}

	return os.WriteFile(path, spec, 0o600)
}

// pkgPrefixOverrides maps package basenames to CamelCase prefixes when
// stripping "types" and capitalizing the first letter is not enough to
// recover word boundaries. Add an entry when the algorithmic prefix reads
// poorly.
var pkgPrefixOverrides = map[string]string{
	"alertmanagertypes":     "AlertManager",
	"cloudintegrationtypes": "CloudIntegration",
	"inframonitoringtypes":  "InfraMonitoring",
	"llmpricingruletypes":   "LLMPricingRule",
	"metricsexplorertypes":  "MetricsExplorer",
	"querybuildertypesv5":   "QueryBuilderV5",
	"rulestatehistorytypes": "RuleStateHistory",
	"serviceaccounttypes":   "ServiceAccount",
	"tracedetailtypes":      "TraceDetail",
}

// defName produces the OpenAPI schema key for a reflected type. It replaces
// the verbose default that swaggest/jsonschema-go produces for generic
// instantiations and for types in "*types" packages.
func defName(t reflect.Type, defaultDefName string) string {
	if defaultDefName == "RenderSuccessResponse" {
		if field, ok := t.FieldByName("Data"); ok {
			return field.Type.Name()
		}
		return defaultDefName
	}

	name := t.Name()
	if strings.Contains(name, "[") && strings.HasSuffix(name, "]") {
		return collapseGenericName(name)
	}
	if name == "" {
		return defaultDefName
	}

	name = capitalize(name)
	prefix := packagePrefix(t.PkgPath())
	// Skip the prefix when the type name already starts with it (case-insensitive).
	// For example, sigv4.SigV4Config becomes SigV4Config rather than Sigv4SigV4Config.
	if prefix == "" || (len(name) >= len(prefix) && strings.EqualFold(name[:len(prefix)], prefix)) {
		return name
	}
	return prefix + name
}

func packagePrefix(pkgPath string) string {
	base := path.Base(pkgPath)
	if base == "." || base == "" || base == "types" {
		return ""
	}
	if v, ok := pkgPrefixOverrides[base]; ok {
		return v
	}
	return capitalize(stripTypesSuffix(base))
}

func stripTypesSuffix(s string) string {
	if rest, ok := strings.CutSuffix(s, "types"); ok {
		return rest
	}
	if i := strings.LastIndex(s, "typesv"); i != -1 {
		if v := s[i+len("typesv"):]; v != "" {
			if _, err := strconv.Atoi(v); err == nil {
				return s[:i] + "V" + v
			}
		}
	}
	return s
}

// collapseGenericName turns a reflect.Type.Name() like
// "QueryBuilderQuery[github.com/SigNoz/.../v5.LogAggregation]" into
// "QueryBuilderQueryLogAggregation" by concatenating the leaf name of the
// base and each type parameter.
func collapseGenericName(typeName string) string {
	open := strings.Index(typeName, "[")
	if open == -1 {
		if i := strings.LastIndex(typeName, "."); i != -1 {
			return typeName[i+1:]
		}
		return typeName
	}
	end := strings.LastIndex(typeName, "]")
	if end <= open {
		return collapseGenericName(typeName[:open])
	}

	var sb strings.Builder
	sb.WriteString(collapseGenericName(typeName[:open]))
	for _, param := range splitTypeParams(typeName[open+1 : end]) {
		sb.WriteString(collapseGenericName(param))
	}
	return sb.String()
}

func splitTypeParams(s string) []string {
	var params []string
	depth, start := 0, 0
	for i, c := range s {
		switch c {
		case '[':
			depth++
		case ']':
			depth--
		case ',':
			if depth == 0 {
				params = append(params, strings.TrimSpace(s[start:i]))
				start = i + 1
			}
		}
	}
	return append(params, strings.TrimSpace(s[start:]))
}

func capitalize(s string) string {
	if s == "" {
		return s
	}
	return strings.ToUpper(s[:1]) + s[1:]
}

// convertJSONNumbers recursively walks a decoded JSON structure and converts
// json.Number values to int64 (preferred) or float64 so that YAML marshaling
// renders them as plain numbers instead of quoted strings.
func convertJSONNumbers(v interface{}) {
	switch val := v.(type) {
	case map[string]interface{}:
		for k, elem := range val {
			if n, ok := elem.(json.Number); ok {
				if i, err := n.Int64(); err == nil {
					val[k] = i
				} else if f, err := n.Float64(); err == nil {
					val[k] = f
				}
			} else {
				convertJSONNumbers(elem)
			}
		}
	case []interface{}:
		for i, elem := range val {
			if n, ok := elem.(json.Number); ok {
				if i64, err := n.Int64(); err == nil {
					val[i] = i64
				} else if f, err := n.Float64(); err == nil {
					val[i] = f
				}
			} else {
				convertJSONNumbers(elem)
			}
		}
	}
}
